class Command(BaseCommand):
    help = "Заполнить БД начальными данными «РЕЗИДЕНТ»"

    def handle(self, *args, **options):
        self.stdout.write("Seeding services…")
        services_data = self._services_payload()
        for row in services_data:
            slug = row.pop("slug")
            Service.objects.update_or_create(slug=slug, defaults=row)

        from apps.services.seed_content import merge_long_articles_into_services

        merge_long_articles_into_services()

        svc = {s.slug: s for s in Service.objects.all()}

        self.stdout.write("Seeding prices…")
        PriceItem.objects.all().delete()
        PriceCategory.objects.all().delete()
        for cat in self._price_categories(svc):
            items = cat.pop("items", [])
            c = PriceCategory.objects.create(**cat)
            for it in items:
                PriceItem.objects.create(category=c, **it)

        self.stdout.write("Seeding promotions…")
        Promotion.objects.all().delete()
        exp = month_end(timezone.now().replace(hour=23, minute=59, second=59))
        for p in self._promotions(exp):
            Promotion.objects.create(**p)

        self.stdout.write("Seeding guarantees…")
        Guarantee.objects.all().delete()
        for g in self._guarantees():
            Guarantee.objects.create(**g)

        self.stdout.write("Seeding reviews…")
        Review.objects.all().delete()
        for r in self._reviews():
            Review.objects.create(**r)

        self.stdout.write("Seeding FAQ…")
        FAQ.objects.all().delete()
        FAQCategory.objects.all().delete()
        for fc in self._faq():
            items = fc.pop("items", [])
            c = FAQCategory.objects.create(**fc)
            for it in items:
                FAQ.objects.create(category=c, **it)

        self.stdout.write("Seeding news…")
        News.objects.all().delete()
        for n in self._news():
            News.objects.create(**n)

        self.stdout.write(self.style.SUCCESS("Готово."))

